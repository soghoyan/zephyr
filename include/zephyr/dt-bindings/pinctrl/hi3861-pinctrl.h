#include <zephyr/sys/util_macro.h>

#define HI3861_PINMUX_PAD_S 0
#define HI3861_PINMUX_PAD_M BIT_MASK(4)

#define HI3861_PINMUX_SIGNAL_S 4
#define HI3861_PINMUX_SIGNAL_M BIT_MASK(3)

#define HI3861_PINMUX_DS_INIT_S 7
#define HI3861_PINMUX_DS_INIT_M BIT_MASK(3)

#define HI3861_PINMUX_DS_MASK_S 10
#define HI3861_PINMUX_DS_MASK_M BIT_MASK(3)

#define HI3861_PINMUX(pad, signal, drive_strength_default, drive_strength_mask)                    \
	(((pad & HI3861_PINMUX_PAD_M) << HI3861_PINMUX_PAD_S) |                                    \
	 ((signal & HI3861_PINMUX_SIGNAL_M) << HI3861_PINMUX_SIGNAL_S) |                           \
	 ((drive_strength_default & HI3861_PINMUX_DS_INIT_M) << HI3861_PINMUX_DS_INIT_S) |         \
	 ((drive_strength_mask & HI3861_PINMUX_DS_MASK_M) << HI3861_PINMUX_DS_MASK_S))

